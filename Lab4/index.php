        // 2. Инициализация требуемого числа колонок
        $colCount = 3;
        
        // 3. Функция формирования строки таблицы
        function getTR($data, $requiredCols) {
            // Разбиваем строку на ячейки по разделителю *
            $cells = explode('*', $data);
            
            $html = '<tr>';
            
            // Формируем требуемое количество ячеек
            for ($i = 0; $i < $requiredCols; $i++) {
                // Если ячейка существует и не пустая - выводим её содержимое
                // Иначе - пустую ячейку
                if (isset($cells[$i]) && trim($cells[$i]) !== '') {
                    $html .= '<td>' . htmlspecialchars($cells[$i]) . '</td>';
                } else {
                    $html .= '<td>&nbsp;</td>';
                }
            }
            
            $html .= '</tr>';
            return $html;
        }
        
        // 4. Функция вывода таблицы
        function outTable($structure, $cols, $num) {
            echo '<h2>Таблица №' . $num . '</h2>';
            
            // Проверка на правильное число колонок
            if ($cols <= 0) {
                echo '<div class="error-msg">Неправильное число колонок</div>';
                return;
            }
            
            // Разбиваем структуру на строки по разделителю #
            $rows = explode('#', $structure);
            
            // Проверка: есть ли строки
            if (empty($rows) || (count($rows) == 1 && trim($rows[0]) === '')) {
                echo '<div class="error-msg">В таблице нет строк</div>';
                return;
            }
            
            // Фильтруем пустые строки и формируем код
            $tableRows = '';
            $hasValidRows = false;
            
            foreach ($rows as $row) {
                // Проверяем, есть ли в строке хоть какие-то ячейки
                $cells = explode('*', $row);
                $hasCells = false;
                
                foreach ($cells as $cell) {
                    if (trim($cell) !== '') {
                        $hasCells = true;
                        break;
                    }
                }
                
                // Если в строке есть непустые ячейки - добавляем её
                if ($hasCells) {
                    $tableRows .= getTR($row, $cols);
                    $hasValidRows = true;
                }
            }
            
            // Проверка: есть ли строки с ячейками
            if (!$hasValidRows) {
                echo '<div class="error-msg">В таблице нет строк с ячейками</div>';
                return;
            }
            
            // Вывод таблицы
            echo '<table>';
            echo $tableRows;
            echo '</table>';
        }
        
        // 5. Циклический вывод всех таблиц
        for ($i = 0; $i < count($structures); $i++) {
            outTable($structures[$i], $colCount, $i + 1);
        }
